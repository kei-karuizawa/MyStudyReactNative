// 9-7：Animated.timing 时间动画函数

// easing：动画函数

// 四种内置动画：

// 1. `Easing.back(3)`：回拉。
// 2. `bounce`：弹跳。
// 3. `ease`：平缓。
// 4. `Easing.elastic(3)`：弹性。

// 三种标准函数：

// 1. `Easing.linear`：一次方函数。
// 2. `Easing.quad`：二次方函数。
// 3. `Easing.cubic`：三次方函数。

// 四种补充函数：

// 1. `Easing.bezier(0,0,1,1)`：贝塞尔。
// 2. `Easing.circle`：环形。
// 3. `Easing.sin`：正弦。
// 4. `Easing.exp`：指数。

// 自由组合动画函数：

// 1. Easing.in(Easing.bounce)：加速+弹跳。
// 2. Easing.out(Easing.exp)：减速+指数。
// 3. Easing.inOut(Easing.elastic(1))：加减速+弹性。

// 所有组合函数效果：https://easings.net/#。
